                        name: 'created_at'
                    },
                    {
                        data: 'action',
                        name: 'action',
                        orderable: false,
                        searchable: false
                    },
                ],
                columnDefs: [{
                    targets: "_all",
                    defaultContent: "",
                }, ],
            });

            // Trigger table redraw on keyup event in the search input field
            search.keyup(function() {
                table.draw();
            });

        });
    </script>
